/**
 * Translation helper function
 * @param {string} key The translation key
 * @param {Object} params Optional parameters to replace in the string
 * @param {string} language The language code
 * @returns {string} The translated string
 */
function getTranslation(key, params = {}, language = 'en') {
  // Get translations for the language or default to English
  const translations = CONTRACT_TRANSLATIONS[language] || CONTRACT_TRANSLATIONS.en;
  
  // Get the translation or return the key if not found
  let text = translations[key] || CONTRACT_TRANSLATIONS.en[key] || key;
  
  // Replace parameters in the string
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`{${param}}`, 'g'), params[param]);
  });
  
  return text;
}

